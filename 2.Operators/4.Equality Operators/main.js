// Strict Equality (type + value)
console.log(1 === 1); //true
console.log("1" === 1); //false

//Loose Equality
//Compares value only
//If type is different it will automatically convert the value
console.log(1 == 1); //true
console.log("1" == 1); //true

console.log("true" == true); //false
console.log(true == "true"); //false
